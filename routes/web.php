Route::view('/', 'welcome')->name('home');

Route::view('/nosotros', 'layouts.nosotros')->name('nosotros');
Route::view('/caracteristicas', 'layouts.caracteristicas')->name('caracteristicas');
Route::view('/beneficios', 'layouts.beneficios')->name('beneficios');
